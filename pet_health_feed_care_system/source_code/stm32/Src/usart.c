void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
	if(huart->Instance == USART2)
	{
		rx2Data = rx2d;
		HAL_UART_Receive_IT(&huart2, &rx2d, 1);
		rx2Flag = 1;
		static int i = 0;
		if(bufferFlage) {
			rx2Buffer[i] = rx2d;
			if (rx2Buffer[i] == '\n') {
				rx2Buffer[i] = '\0';
				completeFlag = 1;
				i = 0;
			} else {
				i++;
			}
		}
	}
}

